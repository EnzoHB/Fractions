// Description of all methods available

Fraction.add(); // Adds all fractions passed to the method and returns the result;
Fraction.sub(); // Subtracts all fractions passed to the method and returns the result;
Fraction.div(); // Divides all fractions passed to the method and returns the result;
Fraction.mul(); // Multiplies all fractions passed to the method and returns the result;
Fraction.gcd(); // Returns the greatest common divisor of all fractions passed to the method;
Fraction.lcm(); // Returns the least common multiple of all fractions passed to the method;
Fraction.min(); // Returns the smallest fraction passed to the method;
Fraction.max(); // Returns de biggest fraction passed to the method;
Fraction.grt(); // Returns true if the first argument is greater than the second and false otherwise;
Fraction.lst(); // Returns true if the first argument is lesser than the second and false otherwise;

Fraction.grte(); // Returns true if the first argument is >= to the second and false otherwise;
Fraction.lste(); // Returns true if the first argument is <= to the second and false otherwise;
Fraction.eqto(); // Returns true if both arguments have the same value;
Fraction.cvrt(); // Converts a decimal or repeating decimal into a fraction and returns it;
